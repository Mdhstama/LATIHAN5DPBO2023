    private void jTombolAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTombolAddActionPerformed
        // TODO add your handling code here:
        
        if (jFieldNama.getText().equals("") || jFieldNIM.getText().equals("") || jFieldNilai.getText().equals("") || btLaki.getText().equals("") || btPerempuan.getText().equals(""))
            JOptionPane.showMessageDialog(null, "Tolong isi Field!");
        else
            if (isUpdate == false)
                insertData();
            else {
                updateData();
                jTombolAdd.setText("Add");
                jTombolDelete.setVisible(false);
                this.isUpdate = false;
                
                //menghilangkan data field setelah di add
                resetForm();
            }
    }//GEN-LAST:event_jTombolAddActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        this.isUpdate = true;

        //Get Selected Data
        int row = jTable1.getSelectedRow();
        String selectedNim = (jTable1.getModel().getValueAt(row, 0).toString());
        String selectedNama = (jTable1.getModel().getValueAt(row, 1).toString());
        String selectedNilai = (jTable1.getModel().getValueAt(row, 2).toString());
        String selectedGender = (jTable1.getModel().getValueAt(row, 3).toString());
        
        for (int i = 0; i < listMhs.size(); i++) {
            if (selectedNim.equals(listMhs.get(i).getNim())) {
                selectedID = i;
                break;
            }
        }

        //System.out.println(selectedID);
        jFieldNIM.setText(selectedNim);
        jFieldNama.setText(selectedNama);
        jFieldNilai.setText(selectedNilai);
        if(btLaki.getText() == "Laki-laki"){
            btLaki.setSelected(true);
        }else if(btPerempuan.getText() == "Perempuan") {
            btPerempuan.setSelected(true);
        }

        jTombolAdd.setText("Update");
        jTombolDelete.setVisible(true);
    }//GEN-LAST:event_jTable1MouseClicked

