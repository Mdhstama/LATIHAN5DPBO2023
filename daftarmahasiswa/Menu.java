    public final DefaultTableModel setTable() {
        Object[] column = {"NIM", "Nama", "Nilai", "Gender"};
        DefaultTableModel dataTabel = new DefaultTableModel(null, column);
        for (int i = 0; i < listMhs.size(); i++) {
            Object[] row = new Object[4];
            row[0] = listMhs.get(i).getNim();
            row[1] = listMhs.get(i).getNama();
            row[2] = listMhs.get(i).getNilai();
            row[3] = listMhs.get(i).getGender();
            dataTabel.addRow(row);
        }
        return dataTabel;
    }

    public void insertData() {
        // Get Data
        String nim = jFieldNIM.getText();
        String nama = jFieldNama.getText();
        String nilai = jFieldNilai.getText();
        String gender = "";
        if(btLaki.isSelected()==true){
            gender = btLaki.getText();
        }else if(btPerempuan.isSelected()==true) {
            gender = btPerempuan.getText();
        }

        listMhs.add(new Mahasiswa(nim, nama, nilai, gender));
        
        //Update Table
        jTable1.setModel(setTable());

        System.out.println("Insert Success!");
        JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan!");
        
        //reset form         
        resetForm();
    }

    public void updateData() {
        // Get Data
        String nim = jFieldNIM.getText();
        String nama = jFieldNama.getText();
        String nilai = jFieldNilai.getText();
        String gender = "";
        if(btLaki.isSelected()==true){
            gender = btLaki.getText();
        }else if(btPerempuan.isSelected()==true) {
            gender = btPerempuan.getText();
        }
        //System.out.println(nim + nama + nilai + gender);

        listMhs.get(selectedID).setNim(nim);
        listMhs.get(selectedID).setNama(nama);
        listMhs.get(selectedID).setNilai(nilai);
        listMhs.get(selectedID).setGender(gender);
        
        //Update Table
        jTable1.setModel(setTable());

        System.out.println("Update Success!");
        JOptionPane.showMessageDialog(null, "Data berhasil diubah!");

        //reset form         
        resetForm();
    }

    public void deleteData() {
        // delete table
        listMhs.get(selectedID).setNim("");
        listMhs.get(selectedID).setNama("");
        listMhs.get(selectedID).setNilai("");
        listMhs.get(selectedID).setGender("");
        
        int confirm;
        confirm = JOptionPane.showConfirmDialog(this, "Yakin Hapus?", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if(confirm == JOptionPane.YES_OPTION){
            for (int i = listMhs.size() - 1; i >= 0; i--) {
                if(listMhs.get(i).getNim().equals(""))
                    listMhs.remove(i);
            }
            System.out.println("Delete Success!");
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
            
            //Update Table
            jTable1.setModel(setTable());
            
            //reset form         
            resetForm();
        }else{
            System.out.println("Delete Failed!");
            JOptionPane.showMessageDialog(null, "Data belum berhasil dihapus!");
            
            //reset form         
            resetForm();
        }
    }
    
